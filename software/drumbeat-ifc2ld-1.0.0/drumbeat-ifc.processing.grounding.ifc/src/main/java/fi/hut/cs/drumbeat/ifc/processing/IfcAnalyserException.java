package fi.hut.cs.drumbeat.ifc.processing;

import fi.hut.cs.drumbeat.ifc.common.IfcException;

public class IfcAnalyserException extends IfcException {
	
	private static final long serialVersionUID = 1L;
	
	

	public IfcAnalyserException() {
	}

	public IfcAnalyserException(String arg0) {
		super(arg0);
	}

	public IfcAnalyserException(Throwable arg0) {
		super(arg0);
	}

	public IfcAnalyserException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IfcAnalyserException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
