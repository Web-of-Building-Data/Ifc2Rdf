package fi.hut.cs.drumbeat.ifc.util.decomposing;

import fi.hut.cs.drumbeat.ifc.processing.IfcAnalyserException;

public class IfcSplittingException extends IfcAnalyserException {

	private static final long serialVersionUID = 1L;

	public IfcSplittingException() {
	}

	public IfcSplittingException(String arg0) {
		super(arg0);
	}

	public IfcSplittingException(Throwable arg0) {
		super(arg0);
	}

	public IfcSplittingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IfcSplittingException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
